// take a Fahrenheit temp and convert it to both Celcius and Kelvin and print those to the screen

let fahrenheit = 50;

// calculate celcius and store
let celcius = (fahrenheit - 32) * (5 / 9);
console.log(celcius);

// calculate Kelvin and store
let kelvin = (fahrenheit + 459.67) * (5 / 9);
console.log(kelvin);
